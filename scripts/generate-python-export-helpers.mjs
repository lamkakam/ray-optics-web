import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");

const sourceFiles = [
  "src/python/src/rayoptics_web_utils/aperture/annular.py",
  "src/python/src/rayoptics_web_utils/aperture/offset_circular.py",
  "src/python/src/rayoptics_web_utils/aperture/offset_rotated_rectangular.py",
];

const outputFile = "src/shared/lib/utils/generated/pythonExportApertureHelpers.ts";
const outputPath = resolve(projectRoot, outputFile);

const helperSource = sourceFiles
  .map((filePath) => readFileSync(resolve(projectRoot, filePath), "utf8"))
  .join("\n");

const output = `// This file is generated by scripts/generate-python-export-helpers.mjs.
// Do not edit by hand.

export const pythonExportApertureHelpers = ${JSON.stringify(helperSource)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output);
